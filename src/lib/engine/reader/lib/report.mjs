/**
 * Report — render the analysis into human-reviewable deliverables.
 *
 * Writes to an output folder:
 *   AUDIT_REPORT.md         master evidence report (the auditor reads/edits this)
 *   audit-result.json       full structured result (machine-readable)
 *   documents/<name>.md     one annotated file per document (sorted, highlighted)
 *
 * Every positive line is a verbatim quote of the client's own text with a line
 * reference. Every gap is stated as a plain "not found". Nothing is invented.
 */
import { mkdir, writeFile, rm } from 'node:fs/promises';
import { join } from 'node:path';

const WEIGHT_TAG = { critical: 'CRITICAL', expected: 'expected' };
const MAX_LIST_ITEMS = 4;

function safeName(rel) {
  return rel.replace(/[\/\\]+/g, '__').replace(/[^\w.\- ]+/g, '_');
}

function pct(n, d) {
  return d === 0 ? '—' : `${Math.round((n / d) * 100)}%`;
}

function joinLabels(items, limit = MAX_LIST_ITEMS) {
  const list = items.filter(Boolean);
  if (!list.length) return 'none';
  const shown = list.slice(0, limit).map((s) => esc(s));
  if (list.length > limit) shown.push(`and ${list.length - limit} more`);
  return shown.join('; ');
}

function topFilesFromSignals(signals, limit = 2) {
  const counts = new Map();
  for (const s of signals) {
    for (const loc of s.locations ?? []) {
      if (!loc.file) continue;
      counts.set(loc.file, (counts.get(loc.file) ?? 0) + 1);
    }
  }
  return [...counts.entries()]
    .sort((a, b) => b[1] - a[1] || a[0].localeCompare(b[0]))
    .slice(0, limit)
    .map(([file, count]) => `${file}${count > 1 ? ` (${count})` : ''}`);
}

function areaCriticalMissing(a) {
  return a.signals.filter((s) => s.weight === 'critical' && !s.met).map((s) => s.label);
}

function areaExpectedMissing(a) {
  return a.signals.filter((s) => s.weight !== 'critical' && !s.met).map((s) => s.label);
}

function areaSummary(a) {
  if (a.docCount === 0) {
    return `No document was matched to Area ${a.area}.`;
  }
  const criticalMissing = areaCriticalMissing(a);
  const expectedMissing = areaExpectedMissing(a);
  let text = `This area has ${a.criticalMet}/${a.criticalTotal} critical signals met and ${a.metCount}/${a.totalCount} signals met overall.`;
  if (criticalMissing.length) {
    text += ` Missing critical items: ${joinLabels(criticalMissing)}.`;
  } else if (expectedMissing.length) {
    text += ` The remaining gaps are expected items: ${joinLabels(expectedMissing)}.`;
  } else {
    text += ' All listed signals are evidenced.';
  }
  const sourceFiles = topFilesFromSignals(a.signals.filter((s) => s.met), 2);
  if (sourceFiles.length) {
    text += ` Main source documents: ${sourceFiles.join('; ')}.`;
  }
  return text;
}

function documentSummary(d) {
  if (!d.readable) {
    return 'This document could not be read as text, so its contents were not assessed.';
  }
  const met = d.signals.filter((s) => s.found);
  const criticalMet = met.filter((s) => s.weight === 'critical').length;
  const totalCritical = d.signals.filter((s) => s.weight === 'critical').length;
  const gaps = d.signals.filter((s) => !s.found);
  const criticalGaps = gaps.filter((s) => s.weight === 'critical').map((s) => s.label);
  const expectedGaps = gaps.filter((s) => s.weight !== 'critical').map((s) => s.label);
  let text = `This document is classified as ${d.classification.ref ?? 'an unmatched file'}${d.classification.title ? ` (${d.classification.title})` : ''}. `;
  if (d.classification.areaName) text += `Area: ${d.classification.area} ${d.classification.areaName}. `;
  text += `Signal coverage is ${criticalMet}/${totalCritical} critical and ${met.length}/${d.signals.length} overall. `;
  if (criticalGaps.length) {
    text += `Critical gaps remain: ${joinLabels(criticalGaps)}.`;
  } else if (expectedGaps.length) {
    text += `Remaining gaps are expected items: ${joinLabels(expectedGaps)}.`;
  } else {
    text += 'All listed signals for this document are evidenced.';
  }
  if (d.redFlags.length) {
    text += ` ${d.redFlags.length} red flag${d.redFlags.length === 1 ? '' : 's'} detected.`;
  }
  return text;
}

