function renderDoc(d) {
  const L = [];
  const p = (s = '') => L.push(s);
  const c = d.classification;

  p(`# ${esc(d.fileName)}`);
  p('');
  if (c.ref) {
    p(`- **Identified as:** ${c.ref} — ${esc(c.title)}  (area ${c.area} · ${esc(c.areaName)})`);
    p(`- **Requirement level:** ${c.requirement ?? '—'}`);
    if (c.basis) p(`- **Regulatory basis:** ${esc(c.basis)}`);
  } else if (c.area) {
    p(`- **Best-guess area:** ${c.area} · ${esc(c.areaName)}`);
  } else {
    p('- **Classification:** could not be determined automatically.');
  }
  p(`- **Match confidence:** ${c.confidence} — ${esc(c.reason)}`);
  p(`- **Lines read:** ${d.lineCount}`);
  if (d.warning) p(`- ⚠️ **Note:** ${esc(d.warning)}`);
  p('');

  if (!d.readable) {
    p('> This document could not be read as text, so its contents were not assessed. Review it manually.');
    p('');
    return L.join('\n');
  }

  p('## Document summary');
  p('');
  p(documentSummary(d));
  p('');

  const met = d.signals.filter((s) => s.found);
  const gaps = d.signals.filter((s) => !s.found);

  p('## Evidence found in this document');
  p('');
  if (!met.length) {
    p('_No area evidence signals matched. Confirm this is the right document for its area._');
  } else {
    for (const s of met) {
      p(`- ✓ **${esc(s.label)}**`);
      for (const cite of s.citations) {
        p(`    - line ${cite.line}: “${esc(cite.quote)}”`);
      }
    }
  }
  p('');

  if (gaps.length) {
    p('## Expected evidence NOT found in this document');
    p('');
    for (const s of gaps) {
      p(`- ✗ ${esc(s.label)}${s.weight === 'critical' ? ' **(critical)**' : ''}`);
    }
    p('');
  }

  p('## Document control');
  p('');
  for (const dc of d.docControl) {
    const cite = dc.found && dc.citation ? ` — line ${dc.citation.line}: “${esc(dc.citation.quote)}”` : '';
    p(`- ${dc.found ? '✓' : '✗'} ${esc(dc.label)}${cite}`);
  }
  p('');

  if (d.redFlags.length) {
    p('## Red flags');
    p('');
    for (const f of d.redFlags) {
      p(`- ⚠️ ${esc(f.label)}${f.severity === 'critical' ? ' (critical)' : ''} — line ${f.line}: “${esc(f.quote)}”`);
    }
    p('');
  }

  if (d.review) {
    p('## Review date');
    p('');
    p(`- Stated: **${d.review.date}** (${d.review.status}) — line ${d.review.line}: “${esc(d.review.quote)}”`);
    p('');
  }

  return L.join('\n');
}

function esc(s) {
  return String(s ?? '').replace(/\|/g, '\\|').replace(/\n/g, ' ');
}
function areaName(result, area) {
  const a = result.areaRollup.find((x) => x.area === area);
  return a ? a.areaName : '';
}

/* -------------------------------------------------------------------------- */
/* Writer                                                                      */
/* -------------------------------------------------------------------------- */

export async function writeReport(result, outDir) {
  await rm(outDir, { recursive: true, force: true });
  await mkdir(join(outDir, 'documents'), { recursive: true });

  await writeFile(join(outDir, 'AUDIT_REPORT.md'), renderMaster(result), 'utf8');
  await writeFile(join(outDir, 'audit-result.json'), JSON.stringify(result, null, 2), 'utf8');

  for (const d of result.perDoc) {
    await writeFile(join(outDir, 'documents', `${safeName(d.file)}.md`), renderDoc(d), 'utf8');
  }

  return {
    master: join(outDir, 'AUDIT_REPORT.md'),
    json: join(outDir, 'audit-result.json'),
    docsDir: join(outDir, 'documents'),
  };
}

export { renderMaster, renderDoc };
